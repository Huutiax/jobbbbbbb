// 颜色混合
export function mixColor(c1, c2, ratio) {
  ratio = Math.max(Math.min(Number(ratio), 1), 0);
  const r1 = parseInt(c1.substring(1, 3), 16);
  const g1 = parseInt(c1.substring(3, 5), 16);
  const b1 = parseInt(c1.substring(5, 7), 16);
  const r2 = parseInt(c2.substring(1, 3), 16);
  const g2 = parseInt(c2.substring(3, 5), 16);
  const b2 = parseInt(c2.substring(5, 7), 16);
  let r = Math.round(r1 * (1 - ratio) + r2 * ratio);
  let g = Math.round(g1 * (1 - ratio) + g2 * ratio);
  let b = Math.round(b1 * (1 - ratio) + b2 * ratio);
  r = ("0" + (r || 0).toString(16)).slice(-2);
  g = ("0" + (g || 0).toString(16)).slice(-2);
  b = ("0" + (b || 0).toString(16)).slice(-2);
  return "#" + r + g + b;
}

// 颜色hex转rgba
export function hex2Rgba(hex, alpha) {
  hex = hex.trim();
  let r, g, b;
  if (/^#/g.test(hex)) {
    r = hex.slice(1, 3);
    g = hex.slice(3, 5);
    b = hex.slice(5, 7);

    if (alpha) {
      return `rgba(${parseInt(r, 16)}, ${parseInt(g, 16)}, ${parseInt(
        b,
        16
      )}, ${alpha})`;
    }
    return `rgb(${parseInt(r, 16)}, ${parseInt(g, 16)}, ${parseInt(b, 16)})`;
  } else {
    return hex;
  }
}

// 近几天时间转化
// 使用：例如 近3天formatRecentTime(-3)
// 当前：formatRecentTime(0)
export function formatRecentTime(day) {
  var today = new Date();
  function doHandleMonth(month) {
    var m = month;
    if (month.toString().length === 1) {
      m = "0" + month;
    }
    return m;
  }
  var targetday_milliseconds = today.getTime() + 1000 * 60 * 60 * 24 * day;
  today.setTime(targetday_milliseconds); // 注意，这行是关键代码
  var tYear = today.getFullYear();
  var tMonth = today.getMonth();
  var tDate = today.getDate();
  tMonth = doHandleMonth(tMonth + 1);
  tDate = doHandleMonth(tDate);
  return tYear + "-" + tMonth + "-" + tDate;
}

export function sleep(interval = 1000) {
  return new Promise((resolve, reject) => {
    let timer = setTimeout(() => {
      resolve();
      clearTimeout(timer);
      timer = null;
    }, interval);
  });
}

export function toFixed(value, decimals = 2) {
  if (isNaN(value)) {
    return "-";
  }
  return Number(Number(value).toFixed(decimals));
}

export function guid() {
  return "xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx".replace(/[xy]/g, function(c) {
    var r = (Math.random() * 16) | 0,
      v = c == "x" ? r : (r & 0x3) | 0x8;
    return v.toString(16);
  });
}

// 间隔一定秒，在min到max区间
export function randomTime(min = 10, max = 30) {
  return Math.ceil(Math.max(min, Math.random() * max)) * 1000;
}
